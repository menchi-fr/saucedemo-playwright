export class CheckoutTwoPage {
  // КЛАСС (чертёж). Большая буква = класс

  constructor(checkoutTwoPage) {
    // ПАРАМЕТР: принимает фикстуру page из теста (new CheckoutTwoPage(page)). Имя параметра я выбрала сама
    this.page = checkoutTwoPage; // this.page — СВОЙСТВО объекта (слева) = параметр checkoutTwoPage (справа). Сохраняю вкладку внутрь объекта
    // справа checkoutTwoPage — параметр конструктора (вкладка из теста). Живёт только внутри конструктора.
    // слева this.page — постоянное свойство объекта: чтобы вкладка не потерялась и методы могли ею пользоваться позже через this.page.

    this.finishButton = checkoutTwoPage.locator('[data-test="finish"]'); // СВОЙСТВО-локатор ← кнопка «Finish»

    // ! Тонкость: локатор пока ничего не ищет на странице — он просто запоминает «как искать». Реальный поиск произойдёт позже, когда метод сделает .click().
  }

  async clickFinish() {
    // метод-действие, БЕЗ параметров: просто нажать Finish
    await this.finishButton.click(); // клик по локатору из конструктора; await — ждём, пока клик отработает
  }

  // Метод = действие страницы. Не копирую в тест, а вызываю: await checkoutTwo.clickFinish();
  // Код действия живёт в POM, тест просто говорит «нажми Finish».
}
