export class CartPage {
  // КЛАСС (чертёж). Большая буква = класс

  constructor(cartPage) {
    // ПАРАМЕТР: принимает фикстуру page, переданную из теста (new CartPage(page)). Имя параметра я выбрала сама
    this.page = cartPage; // this.page — СВОЙСТВО объекта (слева) = параметр cartPage (справа). Сохраняю вкладку внутрь объекта
    // справа cartPage — параметр конструктора (вкладка, прилетевшая из теста). Живёт только внутри конструктора.
    // слева this.page — постоянное свойство объекта: чтобы вкладка не потерялась и методы могли ею пользоваться позже через this.page.

    this.checkoutButton = cartPage.locator('[data-test="checkout"]'); // СВОЙСТВО-локатор ← кнопка «Checkout» (по атрибуту data-test)

    // ! Тонкость: локатор пока ничего не ищет на странице — он просто запоминает «как искать». Реальный поиск произойдёт позже, когда метод сделает .click() по этому локатору.
  }

  // Конструктор = «паспорт страницы»:
  // локаторы — инструкции, как найти элементы (кнопка Checkout и т.д.) ✓
  // this.page — ссылка на вкладку/сайт, чтобы было где искать ✓

  async clickCheckout() {
    // метод-действие, БЕЗ параметров: просто нажать Checkout
    await this.checkoutButton.click(); // кликаем по объявленному в конструкторе локатору; await — ждём, пока клик отработает
  }

  // Методы = действия страницы. Не копирую их в тест, а вызываю: await cart.clickCheckout();
  // Код действия живёт в POM, тест просто говорит «нажми Checkout».
}
